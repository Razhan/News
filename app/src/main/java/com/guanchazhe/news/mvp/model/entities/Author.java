public class Author {

    String pic;

    String name;

    String title;

    public String getPic() {
        return pic;
    }

    public String getName() {
        return name;
    }

    public String getTitle() {
        return title;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
